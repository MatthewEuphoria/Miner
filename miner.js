// Initialize the provider
const provider = new ethers.providers.Web3Provider(window.ethereum);

let wallet;
let contract;
window.isInitialized = false; // Make isInitialized a global variable

async function init() {
    console.log("Initializing...");

    try {
        // Wait for the MetaMask provider to be ready
        await provider.send("eth_requestAccounts", []);
        const signer = provider.getSigner();
        wallet = signer;
        console.log("Wallet connected: ", await wallet.getAddress());

        contract = new ethers.Contract(contractAddress, abi, signer);
        console.log("Contract initialized");

        window.isInitialized = true; // Set isInitialized to true after initialization
        console.log("Initialization complete");
    } catch (error) {
        console.error("Error during initialization:", error);
    }
}

async function mine() {
    if (!window.isInitialized) {
        console.error("Contract is not initialized.");
        return;
    }

    console.log("Mining started...");
    document.getElementById("status").innerText = "Mining started...";
    try {
        const currentChallenge = await contract.currentChallenge();
        const difficulty = await contract.difficulty();
        const address = await wallet.getAddress();

        document.getElementById("challenge").innerText = `Current Challenge: ${currentChallenge}`;
        document.getElementById("difficulty").innerText = `Difficulty: ${difficulty.toString()}`;
        document.getElementById("address").innerText = `Mining with address: ${address}`;

        let nonce = 0;
        while (true) {
            const hash = ethers.utils.keccak256(
                ethers.utils.defaultAbiCoder.encode(
                    ["bytes32", "address", "uint256"],
                    [currentChallenge, address, nonce]
                )
            );
            if (BigInt(hash) < BigInt(difficulty)) {
                console.log(`Found valid nonce: ${nonce}`);
                console.log(`Hash: ${hash}`);
                document.getElementById("status").innerText = `Found valid nonce: ${nonce}`;
                try {
                    const tx = await contract.mine(nonce);
                    console.log("Transaction sent:", tx.hash);
                    document.getElementById("status").innerText = "Transaction sent. Waiting for confirmation...";
                    await tx.wait();
                    console.log("Transaction confirmed");
                    document.getElementById("status").innerText = "Transaction confirmed. Mining complete.";
                    break;
                } catch (error) {
                    console.error("Error submitting transaction:", error);
                    document.getElementById("status").innerText = "Error submitting transaction.";
                    break;
                }
            }
            nonce++;
        }
    } catch (error) {
        console.error("Error during mining:", error);
        document.getElementById("status").innerText = "Error during mining.";
    }
}

// Initialize the DApp
init().catch(console.error);

window.mine = mine; // Export the mine function to the global window object